// Minimal 5x5 font for digits 0-9, dot, O, N, F (for CO2 and ON/OFF)
#ifndef FONT5X5_H
#define FONT5X5_H
#include <stdint.h>
// Each character: 5 columns, 5 rows (bits 0-4)
// Index: '0'-'9' = 0-9, '.'=10, 'O'=11, 'N'=12, 'F'=13
const uint8_t font5x5[][5] = {
  {0x1F,0x11,0x11,0x11,0x1F}, // 0
  {0x00,0x10,0x1F,0x00,0x00}, // 1
  {0x1D,0x15,0x15,0x15,0x17}, // 2
  {0x11,0x15,0x15,0x15,0x1F}, // 3
  {0x07,0x04,0x04,0x04,0x1F}, // 4
  {0x17,0x15,0x15,0x15,0x1D}, // 5
  {0x1F,0x15,0x15,0x15,0x1D}, // 6
  {0x01,0x01,0x01,0x01,0x1F}, // 7
  {0x1F,0x15,0x15,0x15,0x1F}, // 8
  {0x17,0x15,0x15,0x15,0x1F}, // 9
  {0x00,0x00,0x04,0x00,0x00}, // .
  {0x1F,0x11,0x11,0x11,0x1F}, // O
  {0x1F,0x04,0x08,0x10,0x1F}, // N
  {0x1F,0x05,0x05,0x05,0x01}  // F
};
// Map: '0'-'9'->0-9, '.'->10, 'O'->11, 'N'->12, 'F'->13
inline int font5x5_index(char c) {
  if (c >= '0' && c <= '9') return c - '0';
  if (c == '.') return 10;
  if (c == 'O') return 11;
  if (c == 'N') return 12;
  if (c == 'F') return 13;
  return 0; // fallback to '0'
}
#endif // FONT5X5_H
